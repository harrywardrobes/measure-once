#!/usr/bin/env node
/**
 * CI check — validates HubSpot webhook credentials.
 *
 * Fails immediately if HUBSPOT_APP_ID or HUBSPOT_CLIENT_SECRET is absent.
 * When HUBSPOT_ACCESS_TOKEN is also set, performs a live GET to:
 *   https://api.hubapi.com/webhooks/v3/{HUBSPOT_APP_ID}/subscriptions
 * Treats 200 and 404 as pass; 401/403/5xx/network error as fail.
 *
 * Writes test-results/hubspot-credentials.md summarising the outcome.
 */

import { writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';
import { fileURLToPath } from 'url';

const ROOT = join(fileURLToPath(import.meta.url), '..', '..');
const RESULTS_DIR = join(ROOT, 'test-results');
mkdirSync(RESULTS_DIR, { recursive: true });

const HS_BASE = 'https://api.hubapi.com';

const appId         = process.env.HUBSPOT_APP_ID;
const clientSecret  = process.env.HUBSPOT_CLIENT_SECRET;
const accessToken   = process.env.HUBSPOT_ACCESS_TOKEN;

const lines = [];
let failed = false;

function log(msg) {
  process.stdout.write(msg + '\n');
  lines.push(msg);
}

log('# HubSpot credentials check\n');

// ── Presence checks ──────────────────────────────────────────────────────────

if (!appId || appId.trim() === '') {
  log('❌ HUBSPOT_APP_ID is missing or blank');
  failed = true;
} else {
  log(`✅ HUBSPOT_APP_ID is set (${appId})`);
}

if (!clientSecret || clientSecret.trim() === '') {
  log('❌ HUBSPOT_CLIENT_SECRET is missing or blank');
  failed = true;
} else {
  log('✅ HUBSPOT_CLIENT_SECRET is set');
}

// ── Live probe ────────────────────────────────────────────────────────────────

let probeStatus = 'skipped';
let probeDetail = 'HUBSPOT_ACCESS_TOKEN not set — live probe skipped';

if (!failed && accessToken && accessToken.trim() !== '') {
  const url = `${HS_BASE}/webhooks/v3/${encodeURIComponent(appId)}/subscriptions`;
  log(`\nProbing ${url} …`);

  try {
    const resp = await fetch(url, {
      headers: {
        Authorization: `Bearer ${accessToken}`,
        'Content-Type': 'application/json',
      },
      signal: AbortSignal.timeout(10_000),
    });

    const status = resp.status;
    log(`HTTP ${status}`);

    if (status === 200 || status === 404) {
      probeStatus = 'passed';
      probeDetail = `Live GET returned HTTP ${status} (app exists${status === 404 ? ', no subscriptions yet' : ''})`;
      log(`✅ Live probe passed — ${probeDetail}`);
    } else {
      probeStatus = 'failed';
      let body = '';
      try { body = await resp.text(); } catch { /* ignore */ }
      probeDetail = `Live GET returned HTTP ${status}: ${body.slice(0, 200)}`;
      log(`❌ Live probe failed — ${probeDetail}`);
      failed = true;
    }
  } catch (err) {
    probeStatus = 'failed';
    probeDetail = `Network error: ${err.message}`;
    log(`❌ Live probe failed — ${probeDetail}`);
    failed = true;
  }
} else if (!failed) {
  log(`\nℹ️  ${probeDetail}`);
}

// ── Markdown report ───────────────────────────────────────────────────────────

const now = new Date().toISOString();
const overall = failed ? '❌ FAIL' : '✅ PASS';

let md = `# HubSpot Credentials Check — ${now}\n\n`;
md += `## Summary\n\n`;
md += `- Overall: **${overall}**\n`;
md += `- \`HUBSPOT_APP_ID\`: ${appId?.trim() ? `set (\`${appId.trim()}\`)` : '**MISSING**'}\n`;
md += `- \`HUBSPOT_CLIENT_SECRET\`: ${clientSecret?.trim() ? 'set' : '**MISSING**'}\n`;
md += `- \`HUBSPOT_ACCESS_TOKEN\`: ${accessToken?.trim() ? 'set' : 'not set'}\n`;
md += `- Live probe: **${probeStatus}** — ${probeDetail}\n`;
md += `\n---\n_Generated by \`scripts/check-hubspot-credentials.mjs\` at ${now}_\n`;

writeFileSync(join(RESULTS_DIR, 'hubspot-credentials.md'), md, 'utf8');
process.stdout.write(`\nReport written to test-results/hubspot-credentials.md\n`);

process.exit(failed ? 1 : 0);
